void calculaErro(
	double *bufferAcumulado,
	double *bufferMaiorErro,
	float *esperado,
	float *resultado,
	size_t nElementos
) {
	double erro = 0.0;
	double maiorErro = 0.0;
	double aux = 0.0;
	for (size_t i = 0; i<nElementos; i++) {
		aux = esperado[i]-resultado[i];
		if (aux < 0) aux *= -1;
		if (maiorErro < aux) maiorErro = aux;
		erro += aux;
	}
	*bufferAcumulado = erro;
	*bufferMaiorErro = maiorErro;
	return;
}

